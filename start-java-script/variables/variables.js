// javaScript use the keywords var, let and const to declare variables.
var y = 40;  //The var keyword is used in all JavaScript code from 1995 to 2015.
console.log(y); // 40

let x = 50; //If you think the value of the variable can change, use let.
console.log(x);  // 50

z = 90;
console.log(z); // 90

const m = 60; //If you want a general rule: always declare variables with const.
console.log(m);



let price;
price = 10;
console.log('price: ', price);



